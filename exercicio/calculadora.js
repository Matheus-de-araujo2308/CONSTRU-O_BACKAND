function CalSomar(so1, so2){
    return so1+so2
}

function CalSub(su1, su2){
    return su1-su2
}

function CalMult(mul1, mult2){
    return mul1*mult2
}

function CalDiv(div1, div2){
    return div1/div2
}

function CalQua(qua1){
    return qua1*qua1
}

function CalRaiz(raiz1){
    return Math.sqrt(raiz1)
}

module.exports = {
    CalSomar,
    CalSub,
    CalMult,
    CalDiv,
    CalQua,
    CalRaiz
} 